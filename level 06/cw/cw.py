#      1) მომხარებელს შეეკითხეთ ასაკი შემდეგ შეადარეთ მისი ასაკი მეტია თუ არა 18-ზე
age = int(input("რამდენი წლის ხარ? "))

# ასაკის შედარება 18-ზე
if age > 18:
    print("შენ ასაკში ხარ, რომელიც მეტია 18-ზე.")
elif age == 18:
    print("შენ 18 წლის ხარ.")
else:
    print("შენ 18 წელზე ნაკლები ასაკი გაქვს.")
