#3) შექმენით სია და for loop დახმარებით გამოიტანეთ მხოლოდ ლუწი ინდექსებზე მდგომი ელემენტები
# სიის შექმნა
sia = ["ელემენტი1", "ელემენტი2", "ელემენტი3", "ელემენტი4", "ელემენტი5", 
       "ელემენტი6", "ელემენტი7", "ელემენტი8", "ელემენტი9", "ელემენტი10"]

print("ლუწ ინდექსებზე მდებარე ელემენტები:")
# for loop-ის გამოყენებით ლუწ ინდექსებზე მდებარე ელემენტების ამოღება
