#4) შექმენით დიდი სია სადაც გექნებათ ბევრი ელემენტი და შემდეგ გამოიტანთ 5 ის ჯერად ინდექსებზე მდგომ ელემენტებს
# დიდი სიის შექმნა (მაგალითისთვის 50 ელემენტიანი სია)
sia = [f"ელემენტი{i}" for i in range(1, 51)]

print("5-ის ჯერად ინდექსებზე მდებარე ელემენტები:")
# 5-ის ჯერად ინდექსებზე მდებარე ელემენტების ამოღება
for index in range(0, len(sia)):
    if index % 5 == 0:  # შემოწმება, არის თუ არა ინდექსი 5-ის ჯერადი
        print(sia[index])
