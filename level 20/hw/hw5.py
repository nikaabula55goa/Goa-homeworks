#5) შექმენით სია სადაც გექნებათ  ყველა მონაცემთა ტიპის მქონე ელემენტები  შემდეგ for loop ით შეუმოწმებთ მონაცემთა ტიპებს და გამოიტანთ მხოლოდ სტრინგ ტიპის ელემენტებს
# სიის შექმნა სხვადასხვა ტიპის მონაცემებით
sia = [42, "გამარჯობა", 3.14, True, None, "სტრინგი", [1, 2, 3]]

# მხოლოდ სტრინგების ბეჭდვა
for elem in sia:
    if type(elem) == str:  # თუ ელემენტი სტრინგია
        print(elem)
