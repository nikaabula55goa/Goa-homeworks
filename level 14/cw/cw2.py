 # 2) მომხმარებელს შემოატანინეთ რიცხვი შემდეგ კი მომხმარებლის შემოტანილ რიცვამდე დაბეჭდეთ ყველა ლუწი რიცხვი
# მომხმარებლისგან რიცხვის მიღება
number = int(input("შეიტანეთ რიცხვი: "))

# for loop-ის გამოყენება რიცხვების ბეჭდვისთვის
for i in range(1, number + 1):
    if i % 2 == 0:  # თუ რიცხვი ლუწია
        print(i)
