#   1) ერთიდან 50 მდე დაბეჭდეთ რიცხვების საშუალო არითმეტიკული
# რიცხვების 1-დან 50-მდე საშუალო არითმეტიკული
total_sum = 0
count = 0

# for loop-ის გამოყენება რიცხვების შესამოწმებლად
for i in range(1, 51):
    total_sum += i  # რიცხვის დამატება ჯამში
    count += 1  # რიცხვების რაოდენობის მატება

# საშუალო არითმეტიკული
average = total_sum / count

# დაბეჭდვა
print("რიცხვების საშუალო არითმეტიკული 1-დან 50-მდე არის:", average)
