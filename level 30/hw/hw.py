#     1) მომხმარებელს შეეკითხეთ ორი რიცხვი შემდეგ კი შექმენით ფუნქცია რომელსაც არგუმენტად გადასცემთ ამ ორ რიცხვს ხოლო ფუნქცია დააბრუნებს ამ ორი რიცხვის ჯამს, ასევე გააკეთე ასეთი 4 ფუნქცია სხვადასხვა მათემატიკური მოქმედებებისთვის, გამოიყენეთ პარამეტრები და არგუმენტად გადაეცით მომხარებლის შემოტანილი რიცხვები

# ფუნქცია, რომელიც აბრუნებს ორი რიცხვის ჯამს
def add(a, b):
    return a + b

# ფუნქცია, რომელიც აბრუნებს ორი რიცხვის სხვაობას
def subtract(a, b):
    return a - b

# ფუნქცია, რომელიც აბრუნებს ორი რიცხვის ნამრავლს
def multiply(a, b):
    return a * b

# ფუნქცია, რომელიც აბრუნებს ორი რიცხვის განაყოფს
def divide(a, b):
    if b == 0:
        return "გაყოფა 0-ზე შეუძლებელია!"
    return a / b

# მომხმარებლისთვის რიცხვების შეყვანა
num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))

# ფუნქციების გამოძახება და შედეგების ჩვენება
print(f"რიცხვების ჯამი: {add(num1, num2)}")
print(f"რიცხვების სხვაობა: {subtract(num1, num2)}")
print(f"რიცხვების ნამრავლი: {multiply(num1, num2)}")
print(f"რიცხვების განაყოფი: {divide(num1, num2)}")
