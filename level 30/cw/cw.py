# ფუნქცია კვადრატის დასახატად
def draw_square(size):
    """
    ფუნქცია, რომელიც ხატავს კვადრატს მოცემული ზომით.
    :param size: კვადრატის გვერდის ზომა (მთელი რიცხვი)
    """
    for _ in range(size):
        print('* ' * size)  # ხატავს კვადრატის თითო ხაზს

# ფუნქციის გამოძახება სხვადასხვა ზომისთვის
draw_square(3)  # 3x3 ზომის კვადრატი
print()  # ცარიელი ხაზი ვიზუალური გამიჯვნისთვის
draw_square(5)  # 5x5 ზომის კვადრატი
print()
draw_square(7)  # 7x7 ზომის კვადრატი
