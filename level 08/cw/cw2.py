# 1. (x == y) and (z > 5)
x = 3; y = 3; z = 10
print(x == y and z > 5)  # True

# 2. (x != y) and (z <= 10)
x = 3; y = 5; z = 10
print(x != y and z <= 10)  # True

# 3. (x > y) and (z == 10)
x = 5; y = 3; z = 10
print(x > y and z == 10)  # True

# 4. (x < y) and (z >= 5)
x = 3; y = 5; z = 7
print(x < y and z >= 5)  # True

# 5. (x >= y) and (z != 5)
x = 7; y = 5; z = 3
print(x >= y and z != 5)  # True
