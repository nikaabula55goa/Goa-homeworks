#დაწერე პროგრამა, რომელიც ამოწმებს, არის თუ არა რიცხვი ლუწი ან 3-ის ჯერადი.
number = int(input("შეიყვანე რიცხვი: "))

# შემოწმება, არის თუ არა რიცხვი ლუწი ან 3-ის ჯერადი
if number % 2 == 0 or number % 3 == 0:
    print("შემოტანილი რიცხვი ლუწია ან 3-ის ჯერადი.")
else:
    print("შემოტანილი რიცხვი არც ლუწია და არც 3-ის ჯერადი.")
