#მომხმარებლის შემოტანილი რიცხვი შეამოწმე, არის თუ არა  20-ის ჯერადი და დადებითი.
number = int(input("შეიყვანე რიცხვი: "))

# შემოწმება, არის თუ არა რიცხვი 20-ის ჯერადი და დადებითი
if number > 0 and number % 20 == 0:
    print("შემოტანილი რიცხვი 20-ის ჯერადი და დადებითია.")
else:
    print("შემოტანილი რიცხვი ან არ არის 20-ის ჯერადი, ან არ არის დადებითი.")
