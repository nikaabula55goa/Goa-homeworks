#შეამოწმე მომხმარებლის შემოტანილი რიცხვი თუ არის 5-ის ან 10-ის ჯერადი.
number = int(input("შეიყვანე რიცხვი: "))

# შემოწმება, არის თუ არა რიცხვი 5-ის ან 10-ის ჯერადი
if number % 5 == 0 or number % 10 == 0:
    print("შემოტანილი რიცხვი არის 5-ის ან 10-ის ჯერადი.")
else:
    print("შემოტანილი რიცხვი არც 5-ის და არც 10-ის ჯერადი არ არის.")
