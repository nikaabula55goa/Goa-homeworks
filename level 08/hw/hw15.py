#დაწერე პროგრამა, რომელიც ამოწმებს, არის თუ არა რიცხვი ერთდროულად 50-ზე ნაკლები და 10-ის ჯერადი.

number = int(input("შეიყვანე რიცხვი: "))

# შემოწმება, არის თუ არა რიცხვი 50-ზე ნაკლები და 10-ის ჯერადი
if number < 50 and number % 10 == 0:
    print("შემოტანილი რიცხვი არის 50-ზე ნაკლები და 10-ის ჯერადი.")
else:
    print("შემოტანილი რიცხვი ან არ არის 50-ზე ნაკლები, ან არ არის 10-ის ჯერადი.")
