#4) შექმენით სია რომელშიც იქნება რენდომ რიცხვები მოთავსებული შემდეგ კი შექმენით მეორე სია რომელშიც გადაიტანთ პირველი სიიდან მხოლოდ ლუწ რიცხვებს
import random

# შექმენით სია რენდომ რიცხვებით
random_numbers = [random.randint(1, 100) for _ in range(20)]  # 20 რიცხვი 1-100 დიაპაზონში

# შექმენით მეორე სია, რომელშიც მხოლოდ ლუწ რიცხვები გადაინაცვლებს
even_numbers = [num for num in random_numbers if num % 2 == 0]

# შედეგების დაბეჭდვა
print("რენდომ რიცხვები:", random_numbers)
print("ლუწი რიცხვები:", even_numbers)
