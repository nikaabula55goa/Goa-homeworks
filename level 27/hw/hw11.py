#    1) შექმენით სია რომელშიც გექნებათ 1 დან 20-მდე რიცხვები დაბეჭდეთ თითოეული სიის ელემენტი და თითოეულ მათგანს მიუწერეთ ლუწია თუ კენტი, გამოიყენეთ for loop
numbers = list(range(1, 21))
for num in numbers:
    if num % 2 == 0:
        print(f"{num} - ლუწია")
    else:
        print(f"{num} - კენტია")
