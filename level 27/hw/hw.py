#1) for ციკლის მეშვეობით შეასრულეთ ყველა მათემატიკური ოპერაცია 0-დან 100-მდე რიცხვებზე 
for i in range(101):
    print(f"{i}:")
    print(f"  პლუს 2: {i + 2}")
    print(f"  მინუს 2: {i - 2}")
    print(f"  გამრავლებული 2-თან: {i * 2}")
    if i != 0:
        print(f"  გაყოფილი 2-ით: {i / 2}")
    print()
