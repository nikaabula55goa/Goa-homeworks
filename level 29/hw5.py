#6) დაწერეთ რა არის page layout რისთვის ვიყენებთ
#Page Layout (გვერდის განლაგება) არის დიზაინის კონცეფცია, რომელიც განსაზღვრავს, როგორ განლაგდება ინფორმაცია, ელემენტები და ობიექტები გვერდზე. ის გამოიყენება დოკუმენტების, ვებსაიტების, პრეზენტაციების, პუბლიკაციებისა და აპლიკაციების ვიზუალური სტრუქტურის განსაზღვრისთვის.

