def ask_name():
    while True:
        name = input("როგორი სახელით მოგმართო? ")
        if name == 'გაბრიელი':
            print("გამარჯობა, გაბრიელ!")
            break
        else:
            print("გთხოვ, სცადე კიდევ!")

ask_name()
